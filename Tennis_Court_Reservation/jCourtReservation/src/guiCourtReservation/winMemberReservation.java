    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        String str = (String) this.cmbMemberID.getSelectedItem();
        String[] memberReservations = new String[100];
        int k = -1;
        try {
            k = Integer.valueOf(str);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,"Please enter valid n.ยบ Member ID!");}
        if (k != -1) { memberReservations = jReservations.getMemberReservations(k); }
        if (memberReservations.length > 0) {
            String text = jMyTextArea.getText();
            for (int m = 0; m < memberReservations.length; m++) {
                if (memberReservations[m]!=null) {
                    jMyTextArea.append(memberReservations[m] + newline);
                }
            }
        }
    }//GEN-LAST:event_btnFindActionPerformed

