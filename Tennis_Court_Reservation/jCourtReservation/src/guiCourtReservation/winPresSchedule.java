    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        String str = (String) this.cmbCourtID.getSelectedItem();
        String[] courtReservations = new String[100];
        int k = -1;
        try {
            k = Integer.valueOf(str);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,"Please enter valid n.ยบ Court ID!");}
        if (k != -1) { courtReservations = jReservations.presentCourtReservations(k, this.dateFrom); }
        if (courtReservations.length > 0) {
            String text = jMyTextArea.getText();
            for (int m = 0; m < courtReservations.length; m++) {
                if (courtReservations[m]!=null) {
                    jMyTextArea.append(courtReservations[m] + newline);
                }
            }
        }
    }//GEN-LAST:event_btnFindActionPerformed

